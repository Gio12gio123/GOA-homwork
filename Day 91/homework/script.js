// დაწერე for ციკლი, რომელიც გამოიტანს 1-დან 20-მდე ყველა რიცხვს, მაგრამ გამოტოვებს 7-ს და 13-ს.

for (let i = 1; i <= 20; i++) {
  if (i === 7) {
    continue;
  }
  if (i === 13) {
    continue;
  }
  console.log(i);
}

